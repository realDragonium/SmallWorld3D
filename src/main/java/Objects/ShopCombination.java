package Objects;

public class ShopCombination {

    private String race;
    private String power;

    public ShopCombination(){}

    public ShopCombination(String race, String power){
        this.race = race;
        this.power = power;
    }

    public String getRace(){
        return race;
    }

    public String getPower(){
        return power;
    }

}
